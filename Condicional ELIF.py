numero=int(input("¿Qué número queres convertir(entre 1 y 5)? "))

if numero==1:
    print("El número es 'Uno'")
    
elif numero==2:
    print("El número es 'Dos'")
    
elif numero==3:
    print("El número es 'Tres'")
    
elif numero==4:
    print("El número es 'Cuatro'")
    
elif numero==5:
    print("El número es 'Cinco'")
    
else:
    print("No es posible, por favor introducir números enteros entre 1 y 5 (inclusive) solamente. Gracias")
print("Fin.")
